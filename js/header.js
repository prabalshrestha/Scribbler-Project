function opensignUp(){
    
    document.getElementsByClassName("signup-Container")[0].style.display="block";
}

function closesignUp(){
   document.getElementsByClassName("signup-Container")[0].style.display="none";
}

function opensignIn(){
     document.getElementsByClassName("signin-Container")[0].style.display="block";
}

function closesignIn(){
     document.getElementsByClassName("signin-Container")[0].style.display="none";
}


function modalSignUp(){
    document.getElementsByClassName("signin-Container")[0].style.display="none";
    document.getElementsByClassName("signup-Container")[0].style.display="block";
}

function opencreate(){
     document.getElementsByClassName("create-container")[0].style.display="block";
}

function closecreate(){
     document.getElementsByClassName("create-container")[0].style.display="none";
}

